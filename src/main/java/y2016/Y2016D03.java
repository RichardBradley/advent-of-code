    private static boolean isValidTriangle(int[] edges) {
        assert (edges.length == 3);
        return isValidTriangle(edges[0], edges[1], edges[2]);
    }

    private static boolean isValidTriangle(int a, int b, int c) {
        return (a < b + c)
                && (b < a + c)
                && (c < a + b);
    }

    private static List<int[]> getTrianglesFromColumns(int[][] input) {
        List<int[]> acc = new ArrayList<>();
        assert (input.length % 3 == 0);
        for (int i = 0; i < input.length / 3; i++) {
            int x = i * 3;
            acc.add(new int[]{input[x][0], input[x + 1][0], input[x + 2][0]});
            acc.add(new int[]{input[x][1], input[x + 1][1], input[x + 2][1]});
            acc.add(new int[]{input[x][2], input[x + 1][2], input[x + 2][2]});
        }
        return acc;
    }

