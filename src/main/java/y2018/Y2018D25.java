    private static int countConstellations(List<Point4> points) {
        List<List<Point4>> constellations = new ArrayList<>();

        System.out.println("Initial constellaton build");
        outer:
        for (Point4 point : points) {
            for (List<Point4> constellation : constellations) {
                for (Point4 constPoint : constellation) {
                    if (constPoint.dist(point) <= 3) {
                        constellation.add(point);
                        continue outer;
                    }
                }
            }
            // new const:
            List<Point4> newConst = new ArrayList<>();
            newConst.add(point);
            constellations.add(newConst);
        }

        // Now have to merge constellations that didn't form together
        System.out.println("Merging constellations");
        boolean changed;
        mergeLoop:
        do {
            changed = false;
            for (List<Point4> constellation : constellations) {
                for (List<Point4> constellatoin2 : constellations) {
                    if (constellation != constellatoin2) {
                        for (Point4 pointA : constellation) {
                            for (Point4 pointB : constellatoin2) {
                                if (pointA.dist(pointB) <= 3) {
                                    // System.out.println("Merge found");
                                    constellation.addAll(constellatoin2);
                                    constellations.remove(constellatoin2);
                                    changed = true;
                                    continue mergeLoop;
                                }
                            }
                        }
                    }
                }
            }
        } while (changed);

        return constellations.size();
    }

    @Value
    static class Point4 {
        int x;
        int y;
        int z;
        int t;

        int dist(Point4 other) {
            return Math.abs(x - other.x) +
                    Math.abs(y - other.y) +
                    Math.abs(z - other.z) +
                    Math.abs(t - other.t);
        }
    }

    static List<Point4> parse(String spec) {
        return Arrays.stream(spec.split("\n"))
                .map(line -> {
                    String[] coords = line.split(",");
                    checkState(coords.length == 4);
                    return new Point4(
                            Integer.parseInt(coords[0]),
                            Integer.parseInt(coords[1]),
                            Integer.parseInt(coords[2]),
                            Integer.parseInt(coords[3]));
                })
                .collect(Collectors.toList());
    }

