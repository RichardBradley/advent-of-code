    private static long countInRangeOfLargest(List<NanoBot> nanoBots) {
        NanoBot nanoBotWithLargestR = nanoBots.stream()
                .max(Comparator.comparing(b -> b.r))
                .get();

        return nanoBots
                .stream()
                .filter(x -> dist(x, nanoBotWithLargestR) <= nanoBotWithLargestR.r)
                .count();
    }

    private static long dist(NanoBot a, NanoBot b) {
        return Math.abs(a.x - b.x) + Math.abs(a.y - b.y) + Math.abs(a.z - b.z);
    }

